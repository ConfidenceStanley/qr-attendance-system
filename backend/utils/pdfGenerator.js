const PDFDocument = require("pdfkit");
const AttendanceSession = require("../models/AttendanceSession");
const AttendanceRecord = require("../models/AttendanceRecord");
const Course = require("../models/Course");
const Student = require("../models/Student");

const drawLine = (doc) => {
  doc
    .strokeColor("#e4e4e7")
    .lineWidth(1)
    .moveTo(50, doc.y)
    .lineTo(550, doc.y)
    .stroke();
  doc.moveDown(0.5);
};

const drawHeader = (doc, title, subtitle = "") => {
  doc
    .fillColor("#4f46e5")
    .fontSize(20)
    .font("Helvetica-Bold")
    .text("QRoll Attendance System", 50, 50);

  doc
    .fillColor("#71717a")
    .fontSize(10)
    .font("Helvetica")
    .text("HND Computer Science — Automated Attendance Report", 50, 75);

  doc.moveDown(1);
  drawLine(doc);

  doc
    .fillColor("#18181b")
    .fontSize(16)
    .font("Helvetica-Bold")
    .text(title, 50, doc.y);

  if (subtitle) {
    doc
      .fillColor("#71717a")
      .fontSize(10)
      .font("Helvetica")
      .text(subtitle, 50, doc.y + 5);
  }

  doc.moveDown(1);
  drawLine(doc);
};

const drawFooter = (doc) => {
  const bottom = doc.page.height - 50;
  doc
    .fillColor("#71717a")
    .fontSize(9)
    .font("Helvetica")
    .text(
      `Generated by QRoll on ${new Date().toLocaleString()}`,
      50,
      bottom,
      { align: "left" }
    )
    .text("Confidential — For Internal Use Only", 50, bottom, {
      align: "right",
    });
};


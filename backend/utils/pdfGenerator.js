// REPORT 3: Student Report
// ─────────────────────────────────────────
const generateStudentReport = async (res, studentId) => {
  const student = await Student.findById(studentId)
    .populate("user", "fullName")
    .populate("enrolledCourses", "name courseTitle");

  if (!student) throw new Error("Student not found");

  const courseData = await Promise.all(
    (student.enrolledCourses || []).map(async (course) => {
      const sessions = await AttendanceSession.find({
        course: course._id,
        status: "closed",
      });
      const sessionIds = sessions.map((s) => s._id);
      const totalSessions = sessions.length;
      const presentCount = await AttendanceRecord.countDocuments({
        session: { $in: sessionIds },
        student: studentId,
        status: "present",
      });
      const rate =
        totalSessions > 0
          ? Math.round((presentCount / totalSessions) * 100)
          : 0;
      return { course, totalSessions, presentCount, rate };
    })
  );

  const doc = new PDFDocument({ margin: 50 });

  res.setHeader("Content-Type", "application/pdf");
  res.setHeader(
    "Content-Disposition",
    `attachment; filename="student-report-${studentId}.pdf"`
  );
  doc.pipe(res);

  const fullName = student.user?.fullName || "Unknown Student";

  drawHeader(
    doc,
    "Student Attendance Report",
    `${fullName} — ${student.studentId}`
  );

  const info = [
    ["Full Name", fullName],
    ["Student ID", student.studentId || "N/A"],
    ["Level", student.level || "N/A"],
    ["Department", student.department || "N/A"],
  ];

  info.forEach(([label, value]) => {
    doc
      .fillColor("#71717a")
      .fontSize(10)
      .font("Helvetica-Bold")
      .text(`${label}: `, 50, doc.y, { continued: true })
      .fillColor("#18181b")
      .font("Helvetica")
      .text(String(value));
  });

  doc.moveDown(1);
  drawLine(doc);

  doc
    .fillColor("#18181b")
    .fontSize(12)
    .font("Helvetica-Bold")
    .text("Course Attendance Breakdown", 50, doc.y);
  doc.moveDown(0.5);

  doc.fillColor("#4f46e5").fontSize(10).font("Helvetica-Bold");
  doc.text("S/N", 50, doc.y, { width: 35 });
  doc.text("Course Code", 85, doc.y - 12, { width: 100 });
  doc.text("Course Name", 185, doc.y - 12, { width: 180 });
  doc.text("Sessions", 365, doc.y - 12, { width: 70 });
  doc.text("Present", 435, doc.y - 12, { width: 65 });
  doc.text("Rate", 500, doc.y - 12, { width: 60 });
  doc.moveDown(0.3);
  drawLine(doc);

  courseData.forEach(({ course, totalSessions, presentCount, rate }, index) => {
    const y = doc.y;
    const rateColor = rate >= 75 ? "#10b981" : "#f43f5e";

    doc.fillColor("#18181b").fontSize(9).font("Helvetica");
    doc.text(String(index + 1), 50, y, { width: 35 });
    doc.text(course.courseCode || "N/A", 85, y, { width: 100 });
    doc.text(course.courseTitle || "N/A", 185, y, { width: 180 });
    doc.text(String(totalSessions), 365, y, { width: 70 });
    doc.text(String(presentCount), 435, y, { width: 65 });
    doc.fillColor(rateColor).text(`${rate}%`, 500, y, { width: 60 });
    doc.moveDown(0.4);
  });

  drawFooter(doc);
  doc.end();
};


// REPORT 2: Course Report
// ─────────────────────────────────────────
const generateCourseReport = async (res, courseId, startDate, endDate) => {
  const course = await Course.findById(courseId);
  if (!course) throw new Error("Course not found");

  const query = { course: courseId };
  if (startDate && endDate) {
    query.createdAt = {
      $gte: new Date(startDate),
      $lte: new Date(endDate),
    };
  }

  const sessions = await AttendanceSession.find(query).sort({ createdAt: -1 });

  const sessionData = await Promise.all(
    sessions.map(async (session) => {
      const records = await AttendanceRecord.find({ session: session._id });
      const present = records.filter((r) => r.status === "present").length;
      const total = records.length;
      const rate = total > 0 ? Math.round((present / total) * 100) : 0;
      return { session, present, total, rate };
    })
  );

  const overallPresent = sessionData.reduce((sum, s) => sum + s.present, 0);
  const overallTotal = sessionData.reduce((sum, s) => sum + s.total, 0);
  const overallRate =
    overallTotal > 0
      ? Math.round((overallPresent / overallTotal) * 100)
      : 0;

  const doc = new PDFDocument({ margin: 50 });

  res.setHeader("Content-Type", "application/pdf");
  res.setHeader(
    "Content-Disposition",
    `attachment; filename="course-report-${courseId}.pdf"`
  );
  doc.pipe(res);

  drawHeader(
    doc,
    "Course Attendance Report",
    `${course.courseCode} — ${course.courseTitle}`
  );

  const info = [
    ["Course Code", course.courseCode],
    ["Course Name", course.courseTitle],
    ["Total Sessions", sessions.length],
    ["Overall Attendance Rate", `${overallRate}%`],
  ];

  info.forEach(([label, value]) => {
    doc
      .fillColor("#71717a")
      .fontSize(10)
      .font("Helvetica-Bold")
      .text(`${label}: `, 50, doc.y, { continued: true })
      .fillColor("#18181b")
      .font("Helvetica")
      .text(String(value));
  });

  doc.moveDown(1);
  drawLine(doc);

  doc
    .fillColor("#18181b")
    .fontSize(12)
    .font("Helvetica-Bold")
    .text("Session Breakdown", 50, doc.y);
  doc.moveDown(0.5);

  doc.fillColor("#4f46e5").fontSize(10).font("Helvetica-Bold");
  doc.text("S/N", 50, doc.y, { width: 35 });
  doc.text("Topic", 85, doc.y - 12, { width: 160 });
  doc.text("Date", 245, doc.y - 12, { width: 90 });
  doc.text("Present", 335, doc.y - 12, { width: 65 });
  doc.text("Total", 400, doc.y - 12, { width: 55 });
  doc.text("Rate", 455, doc.y - 12, { width: 60 });
  doc.text("Status", 515, doc.y - 12, { width: 60 });
  doc.moveDown(0.3);
  drawLine(doc);

  sessionData.forEach(({ session, present, total, rate }, index) => {
    const y = doc.y;
    const rateColor = rate >= 75 ? "#10b981" : "#f43f5e";

    doc.fillColor("#18181b").fontSize(9).font("Helvetica");
    doc.text(String(index + 1), 50, y, { width: 35 });
    doc.text(session.topic || "N/A", 85, y, { width: 160 });
    doc.text(
      new Date(session.createdAt).toLocaleDateString(),
      245,
      y,
      { width: 90 }
    );
    doc.text(String(present), 335, y, { width: 65 });
    doc.text(String(total), 400, y, { width: 55 });
    doc.fillColor(rateColor).text(`${rate}%`, 455, y, { width: 60 });
    doc
      .fillColor("#18181b")
      .text(session.status?.toUpperCase() || "N/A", 515, y, { width: 60 });
    doc.moveDown(0.4);
  });

  drawFooter(doc);
  doc.end();
};


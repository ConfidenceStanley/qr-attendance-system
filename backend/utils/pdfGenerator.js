// REPORT 1: Session Report
// ─────────────────────────────────────────
const generateSessionReport = async (res, sessionId) => {
  const session = await AttendanceSession.findById(sessionId)
    .populate("course", "name courseCode")
    .populate({
      path: "lecturer",
      populate: { path: "user", select: "fullName" },
    });

  if (!session) throw new Error("Session not found");

  // Populate student → then student.user for fullName
  const records = await AttendanceRecord.find({ session: sessionId })
    .populate({
      path: "student",
      select: "studentId level",
      populate: { path: "user", select: "fullName" },
    });

  const presentRecords = records.filter((r) => r.status === "present");
  const absentRecords = records.filter((r) => r.status === "absent");
  const totalStudents = records.length;
  const attendanceRate =
    totalStudents > 0
      ? Math.round((presentRecords.length / totalStudents) * 100)
      : 0;

  const doc = new PDFDocument({ margin: 50 });

  res.setHeader("Content-Type", "application/pdf");
  res.setHeader(
    "Content-Disposition",
    `attachment; filename="session-report-${sessionId}.pdf"`
  );
  doc.pipe(res);

  drawHeader(
    doc,
    "Session Attendance Report",
    `${session.course?.courseCode} — ${session.course?.name}`
  );

  // Session info
  const lecturerName = session.lecturer?.user?.fullName || "N/A";
  const info = [
    ["Course", `${session.course?.courseCode} — ${session.course?.name}`],
    ["Lecturer", lecturerName],
    ["Topic", session.topic || "N/A"],
    ["Date", new Date(session.createdAt).toLocaleDateString()],
    ["Status", session.status?.toUpperCase()],
    ["Total Students", totalStudents],
    ["Present", presentRecords.length],
    ["Absent", absentRecords.length],
    ["Attendance Rate", `${attendanceRate}%`],
  ];

  info.forEach(([label, value]) => {
    doc
      .fillColor("#71717a")
      .fontSize(10)
      .font("Helvetica-Bold")
      .text(`${label}: `, 50, doc.y, { continued: true })
      .fillColor("#18181b")
      .font("Helvetica")
      .text(String(value));
  });

  doc.moveDown(1);
  drawLine(doc);

  // ── Present Students ──
  doc
    .fillColor("#10b981")
    .fontSize(12)
    .font("Helvetica-Bold")
    .text(`✓ Present Students (${presentRecords.length})`, 50, doc.y);
  doc.moveDown(0.5);

  if (presentRecords.length === 0) {
    doc
      .fillColor("#71717a")
      .fontSize(10)
      .font("Helvetica")
      .text("No students marked present.", 50, doc.y);
  } else {
    doc.fillColor("#4f46e5").fontSize(10).font("Helvetica-Bold");
    doc.text("S/N", 50, doc.y, { width: 40 });
    doc.text("Name", 90, doc.y - 12, { width: 180 });
    doc.text("Matric No.", 270, doc.y - 12, { width: 120 });
    doc.text("Level", 390, doc.y - 12, { width: 60 });
    doc.text("Scan Time", 450, doc.y - 12, { width: 100 });
    doc.moveDown(0.3);
    drawLine(doc);

    presentRecords.forEach((record, index) => {
      const s = record.student;
      const fullName = s?.user?.fullName || "Unknown";
      const y = doc.y;
      doc.fillColor("#18181b").fontSize(9).font("Helvetica");
      doc.text(String(index + 1), 50, y, { width: 40 });
      doc.text(fullName, 90, y, { width: 180 });
      doc.text(s?.studentId || "N/A", 270, y, { width: 120 });
      doc.text(s?.level || "N/A", 390, y, { width: 60 });
      doc.text(
        record.scannedAt
          ? new Date(record.scannedAt).toLocaleTimeString()
          : "N/A",
        450,
        y,
        { width: 100 }
      );
      doc.moveDown(0.4);
    });
  }

  doc.moveDown(1);
  drawLine(doc);

  // ── Absent Students ──
  doc
    .fillColor("#f43f5e")
    .fontSize(12)
    .font("Helvetica-Bold")
    .text(`✗ Absent Students (${absentRecords.length})`, 50, doc.y);
  doc.moveDown(0.5);

  if (absentRecords.length === 0) {
    doc
      .fillColor("#71717a")
      .fontSize(10)
      .font("Helvetica")
      .text("No students absent.", 50, doc.y);
  } else {
    doc.fillColor("#4f46e5").fontSize(10).font("Helvetica-Bold");
    doc.text("S/N", 50, doc.y, { width: 40 });
    doc.text("Name", 90, doc.y - 12, { width: 180 });
    doc.text("Matric No.", 270, doc.y - 12, { width: 120 });
    doc.text("Level", 390, doc.y - 12, { width: 60 });
    doc.moveDown(0.3);
    drawLine(doc);

    absentRecords.forEach((record, index) => {
      const s = record.student;
      const fullName = s?.user?.fullName || "Unknown";
      const y = doc.y;
      doc.fillColor("#18181b").fontSize(9).font("Helvetica");
      doc.text(String(index + 1), 50, y, { width: 40 });
      doc.text(fullName, 90, y, { width: 180 });
      doc.text(s?.studentId || "N/A", 270, y, { width: 120 });
      doc.text(s?.level || "N/A", 390, y, { width: 60 });
      doc.moveDown(0.4);
    });
  }

  drawFooter(doc);
  doc.end();
};

